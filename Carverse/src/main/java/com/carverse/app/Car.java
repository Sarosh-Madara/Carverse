package com.carverse.app;

public class Car {
    private int id;
    private String vehicle_id;
    private String make;
    private String model;
    private String year;
    private String reset_procedure;
    private String reset_procedure2;
    private String video_link;
    private String status;
    private String created_at;
    private String updated_at;

    // Getters and setters
    public int getId() { return id; }
    public void setId(int id) { this.id = id; }
    public String getVehicle_id() { return vehicle_id; }
    public void setVehicle_id(String vehicle_id) { this.vehicle_id = vehicle_id; }
    public String getMake() { return make; }
    public void setMake(String make) { this.make = make; }
    public String getModel() { return model; }
    public void setModel(String model) { this.model = model; }
    public String getYear() { return year; }
    public void setYear(String year) { this.year = year; }
    public String getReset_procedure() { return reset_procedure; }
    public void setReset_procedure(String reset_procedure) { this.reset_procedure = reset_procedure; }
    public String getReset_procedure2() { return reset_procedure2; }
    public void setReset_procedure2(String reset_procedure2) { this.reset_procedure2 = reset_procedure2; }
    public String getVideo_link() { return video_link; }
    public void setVideo_link(String video_link) { this.video_link = video_link; }
    public String getStatus() { return status; }
    public void setStatus(String status) { this.status = status; }
    public String getCreated_at() { return created_at; }
    public void setCreated_at(String created_at) { this.created_at = created_at; }
    public String getUpdated_at() { return updated_at; }
    public void setUpdated_at(String updated_at) { this.updated_at = updated_at; }
 }